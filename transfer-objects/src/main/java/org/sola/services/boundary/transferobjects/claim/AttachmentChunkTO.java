package org.sola.services.boundary.transferobjects.claim;

import java.util.Date;
import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import org.sola.services.common.contracts.AbstractReadWriteTO;

@XmlRootElement(name = "attachmentChunk")
@XmlAccessorType(XmlAccessType.NONE)
public class AttachmentChunkTO extends AbstractReadWriteTO {
    
    @XmlElement
    private String id;
    @XmlElement
    private String attachmentId;
    @XmlElement
    private String claimId;
    @XmlElement
    private long startPosition;
    @XmlElement
    private long size;
    @XmlElement
    private String md5;
    private String userName;
    private Date creationTime;
    
    public AttachmentChunkTO(){
        super();
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAttachmentId() {
        return attachmentId;
    }

    public void setAttachmentId(String attachmentId) {
        this.attachmentId = attachmentId;
    }

    public String getClaimId() {
        return claimId;
    }

    public void setClaimId(String claimId) {
        this.claimId = claimId;
    }

    public long getStartPosition() {
        return startPosition;
    }

    public void setStartPosition(long startPosition) {
        this.startPosition = startPosition;
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public Date getCreationTime() {
        return creationTime;
    }

    public void setCreationTime(Date creationTime) {
        this.creationTime = creationTime;
    }
}
